    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // Get the database connection from the controller
            java.sql.Connection conn = DatabaseConnection.getConnection();

            // Check if connection is valid
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Tidak dapat terhubung ke database",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Lokasi file .jasper (hasil compile dari .jrxml)
            String reportPath = "./src/report/ReportSopir.jasper";

            // Mengisi laporan dengan data dari database
            JasperPrint print = JasperFillManager.fillReport(reportPath, null, conn);

            // Menampilkan laporan dengan viewer
            JasperViewer.viewReport(print, false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak laporan: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            System.err.println("Error mencetak laporan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btntambahActionPerformed
        try {
            // Validate inputs
            if (txtnama.getText().trim().isEmpty()
                    || txtno.getText().trim().isEmpty()
                    || txttarif.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama, No SIM, dan Tarif per Hari harus diisi!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Get values from form
            String nama = txtnama.getText().trim();
            String noSim = txtno.getText().trim();
            int tarifPerHari = parseTarif(txttarif.getText().trim());

            // Call controller to add sopir - status is automatically set to "tersedia"
            boolean success = sopirController.addSopir(nama, noSim, tarifPerHari);

            if (success) {
                JOptionPane.showMessageDialog(this, "Data sopir berhasil ditambahkan",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);

                // Refresh table data
                loadData();

                // Clear form for new input
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data sopir",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            System.err.println("Error menambahkan sopir: " + e.getMessage());
        }
    }// GEN-LAST:event_btntambahActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btneditActionPerformed
        try {
            // Check if any row is selected
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih baris data yang akan diedit!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check if ID is provided
            if (txtid.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID sopir tidak valid!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validate inputs
            if (txtnama.getText().trim().isEmpty()
                    || txtno.getText().trim().isEmpty()
                    || txttarif.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama, No SIM, dan Tarif per Hari harus diisi!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Get values from form
            int idSopir = Integer.parseInt(txtid.getText().trim());
            String nama = txtnama.getText().trim();
            String noSim = txtno.getText().trim();
            int tarifPerHari = parseTarif(txttarif.getText().trim());
            String status = jRadioButton1.isSelected() ? "tersedia" : "dalam tugas";

            // Call controller to update sopir
            boolean success = sopirController.updateSopir(idSopir, nama, noSim, tarifPerHari, status);

            if (success) {
                JOptionPane.showMessageDialog(this, "Data sopir berhasil diperbarui",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);

                // Refresh table data
                loadData();

                // Clear form
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data sopir",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            System.err.println("Error mengedit sopir: " + e.getMessage());
        }
    }// GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnhapusActionPerformed
        try {
            // Check if any row is selected
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih baris data yang akan dihapus!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check if ID is provided
            if (txtid.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID sopir tidak valid!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Confirm deletion
            int confirm = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus data sopir ini?",
                    "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                int idSopir = Integer.parseInt(txtid.getText().trim());

                // Call controller to delete sopir
                boolean success = sopirController.deleteSopir(idSopir);

                if (success) {
                    JOptionPane.showMessageDialog(this, "Data sopir berhasil dihapus",
                            "Sukses", JOptionPane.INFORMATION_MESSAGE);

                    // Refresh table data
                    loadData();

                    // Clear form
                    clearForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data sopir",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            System.err.println("Error menghapus sopir: " + e.getMessage());
        }
    }// GEN-LAST:event_btnhapusActionPerformed

