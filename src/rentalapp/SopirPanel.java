    private void setupTable() {
        // Define table column names
        String[] columnNames = {
            "ID", "Nama", "No SIM", "Tarif per Hari", "Status"
        };

        // Create table model with defined column names
        tableModel = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Make table read-only
            }
        };

        // Set the model to the table
        jTable1.setModel(tableModel);

        // Add mouse listener to handle row selection
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                tableRowSelected();
            }
        });
    }

    private void loadData() {
        // Clear existing data
        tableModel.setRowCount(0);

        // Get all sopir data
        List<Sopir> sopirList = sopirController.getAllSopir();

        // Populate table with data
        for (Sopir sopir : sopirList) {
            Object[] row = {
                sopir.getIdSopir(),
                sopir.getNama(),
                sopir.getNoSim(),
                sopir.getTarifPerHari(),
                sopir.getStatus()
            };
            tableModel.addRow(row);
        }
    }

    private void clearForm() {
        txtid.setText("");
        txtnama.setText("");
        txtno.setText(""); // No SIM
        txttarif.setText(""); // Tarif per hari

        // Default status to "tersedia"
        jRadioButton1.setSelected(true);
    }

    private void tableRowSelected() {
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {
            // Get values from selected row
            int idSopir = Integer.parseInt(tableModel.getValueAt(selectedRow, 0).toString());
            String nama = tableModel.getValueAt(selectedRow, 1).toString();
            String noSim = tableModel.getValueAt(selectedRow, 2).toString();
            int tarifPerHari = Integer.parseInt(tableModel.getValueAt(selectedRow, 3).toString());
            String status = tableModel.getValueAt(selectedRow, 4).toString();

            // Populate form fields
            txtid.setText(String.valueOf(idSopir));
            txtnama.setText(nama);
            txtno.setText(noSim); // No SIM
            txttarif.setText(String.valueOf(tarifPerHari)); // Tarif per hari

            // Set radio button based on status
            if ("tersedia".equals(status)) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }
        }
    }

    private int parseTarif(String tarifStr) throws NumberFormatException {
        // Remove any non-numeric characters
        String cleanStr = tarifStr.replaceAll("[^\\d]", "");
        return Integer.parseInt(cleanStr);
    }

